export function t(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  let s = T[lang][key] ?? T.en[key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      s = s.replace(`{${k}}`, String(v));
    });
  }
  return s;
}

export function freeQuestionsLabel(lang: Lang, n: number): string {
  if (lang === 'ur') return `${n} مفت سوالات باقی`;
  return `${n} free question${n === 1 ? '' : 's'} left`;
}

export function gateMessage(lang: Lang): string {
  return T[lang].gateMessage;
}
